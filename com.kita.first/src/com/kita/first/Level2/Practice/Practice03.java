package com.kita.first.Level2.Practice;

public class Practice03 {
	private String name;
	private int age;
	
	
	public Practice03(String name, int age) {
		this.name = name;
		this.age = age;
	}
	
	void setName(String name) {
		this.name = name;
	}
	
	void setAge(int age) {
		this.age = age;
	}
	
	String getName() {
		return name;
	}
	
	int getAge() {
		return age;
	}
	
	
	
	
}
